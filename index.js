function operacoesBasicas(numero1, numero2) {
    // Adição
    console.log(`${numero1} + ${numero2} = ${numero1 + numero2}`);
    
    // Subtração
    console.log(`${numero1} - ${numero2} = ${numero1 - numero2}`);
    
    // Multiplicação
    console.log(`${numero1} x ${numero2} = ${numero1 * numero2}`);
    
    // Divisão
    console.log(`${numero1} / ${numero2} = ${numero1 / numero2}`);
}


// Exemplo de uso da função
operacoesBasicas(4, 5);